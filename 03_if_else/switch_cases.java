public class switch_cases {
    public static void main(String args[])
    {
        int x = 2;
        switch(x)
        {
            case 1:
            System.out.println("Value is 1 ");
            break;

            case 2:
            System.out.println("Value is 2 ");
            break;

            case 3:
            System.out.println("Value is 3 ");
            break;
            
            default:
            System.out.println("Value is other than 1, 2 or 3.");
            break;
        }
    }
}
