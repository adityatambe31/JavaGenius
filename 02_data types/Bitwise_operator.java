public class Bitwise_operator {
    public static void main(String args[])
    {
        int a = 10;
        int b = 20;

        System.out.println(a&b);
    
        a = 5;
        b = 3;
        System.out.println(a|b);

        a = 5;
        b = 3;
        System.out.println(a^b);
        
        
        int c = 20;
        System.out.println(~c);
    }
}
