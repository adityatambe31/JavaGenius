// pre increment a++/a-- first print then increment
// post increment ++a/--a first increment then print 


public class Unary {
    public static void main(String args[])
    {
        int a = 10;
        System.out.println(a++);
        System.out.println(a);
        System.out.println(a--);
        System.out.println(a);
    }
}
